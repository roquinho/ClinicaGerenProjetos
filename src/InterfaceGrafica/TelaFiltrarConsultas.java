    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Long cpf = new Long(11);
        cpf = Long.parseLong(cpfPacienteText.getText());        
        try {
            Fachada fachada = new FachadaImplementa();
            Consultas consulta = fachada.filtrarConsultaCpfPaciente(cpf);
            jTable2.setValueAt(consulta.getPaciente().getNome(), 0, 0);
            jTable2.setValueAt(consulta.getPaciente().getCpf(), 0, 1);
            jTable2.setValueAt(consulta.getDiaConsulta(), 0, 2);
            jTable2.setValueAt(consulta.getHoraConsulta(), 0, 3);
            jTable2.setValueAt(consulta.getMedico().getNome(), 0, 4);
  
        } catch (ExceptionRegraNegocioFiltrarConsultas ex) {
            Logger.getLogger(TelaFiltrarConsultas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

